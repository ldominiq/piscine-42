#include <unistd.h>

void	ft_is_negative(int n)
{
	char	neg;
	char	pos;

	neg = 'N';
	pos = 'P';
	if (n >= 0)
		write(1, &pos, 1);
	else if (n < 0)
		write(1, &neg, 1);
}
